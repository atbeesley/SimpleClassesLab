public class Calculator {

    public int add(int firstInput, int secondInput){
        return firstInput + secondInput;
    }

    public int subtract(int firstInput, int secondInput){
        return firstInput - secondInput;
    }

    public int multiply(int firstInput, int secondInput){
        return firstInput * secondInput;
    }

    public double divide(double firstInput, double secondInput){
        return firstInput / secondInput;
    }
}
